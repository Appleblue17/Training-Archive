bool vis[N];
int pr[N],id;
void init(int lim){
    for(int i=2;i<=lim;i++){
        if(!vis[i]) pr[++id]=i;
        for(int j=1;j<=id && i*pr[j]<=lim;j++){
            vis[i*pr[j]]=1;
            if(i%pr[j]==0) break;
        }
    }
}


